// Filtrar artículos
export const filtrarArticulos = (filtros) => {
  let articulos = getArticulosBlog();
  
  if (filtros.categoria && filtros.categoria !== 'Todas') {
    articulos = articulos.filter(a => a.categoria === filtros.categoria);
  }
  
  if (filtros.tipo === 'destacados') {
    articulos = articulos.filter(a => a.destacado);
  } else if (filtros.tipo === 'premium') {
    articulos = articulos.filter(a => a.premium);
  } else if (filtros.tipo === 'gratuitos') {
    articulos = articulos.filter(a => !a.premium);
  }
  
  if (filtros.busqueda) {
    const busqueda = filtros.busqueda.toLowerCase();
    articulos = articulos.filter(a => 
      a.titulo.toLowerCase().includes(busqueda) ||
      a.resumen.toLowerCase().includes(busqueda) ||
      a.tags.some(tag => tag.toLowerCase().includes(busqueda))
    );
  }
  
  return articulos;
};

