// Obtener categorías únicas
export const getCategoriasPodcast = () => {
  const episodios = getEpisodiosPodcast();
  const categorias = [...new Set(episodios.map(e => e.categoria))];
  return ['Todas', ...categorias];
};

// Obtener tipos únicos
export const getTiposPodcast = () => {
  const episodios = getEpisodiosPodcast();
  const tipos = [...new Set(episodios.map(e => e.tipo))];
  return ['Todos', ...tipos];
};

// Obtener episodio por ID
export const getEpisodioById = (id) => {
  const episodios = getEpisodiosPodcast();
  return episodios.find(e => e.id === id);
};

// Filtrar episodios
export const filtrarEpisodios = (filtros) => {
  let episodios = getEpisodiosPodcast();
  
  if (filtros.categoria && filtros.categoria !== 'Todas') {
    episodios = episodios.filter(e => e.categoria === filtros.categoria);
  }
  
  if (filtros.tipo && filtros.tipo !== 'Todos') {
    episodios = episodios.filter(e => e.tipo === filtros.tipo);
  }
  
  if (filtros.destacados) {
    episodios = episodios.filter(e => e.destacado);
  }
  
  if (filtros.premium === 'si') {
    episodios = episodios.filter(e => e.premium);
  } else if (filtros.premium === 'no') {
    episodios = episodios.filter(e => !e.premium);
  }
  
  if (filtros.busqueda) {
    const busqueda = filtros.busqueda.toLowerCase();
    episodios = episodios.filter(e => 
      e.titulo.toLowerCase().includes(busqueda) ||
      e.descripcion.toLowerCase().includes(busqueda) ||
      e.tags.some(tag => tag.toLowerCase().includes(busqueda))
    );
  }
  
  return episodios;
};

// Obtener episodios destacados
export const getEpisodiosDestacados = () => {
  return getEpisodiosPodcast().filter(e => e.destacado);
};

// Obtener episodios recientes
export const getEpisodiosRecientes = (limit = 5) => {
  return getEpisodiosPodcast()
    .sort((a, b) => new Date(b.fecha) - new Date(a.fecha))
    .slice(0, limit);
};

// Obtener episodios más populares
export const getEpisodiosPopulares = (limit = 5) => {
  return getEpisodiosPodcast()
    .sort((a, b) => b.reproducciones - a.reproducciones)
    .slice(0, limit);
};

